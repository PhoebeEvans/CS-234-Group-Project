        // TODO add your handling code here:
        String nameValue = nameField.getText();
        String speakerValue = speakerField.getText();
        String locationValue = locationField.getText();
        String dateValue = dateField.getText();
        Program prog = new Program(speakerValue, nameValue, dateValue, locationValue, new ArrayList<String>());
  
          int answer = JOptionPane.showConfirmDialog(null, "Are you sure you want to add the program named : " + prog.eventName +  " ?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
            
            if(answer == 0){
                
                 ProgramDataStore.Programs.add(prog);
                 dispose();
            }
